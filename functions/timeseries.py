    p_value = np.round(p_value,3)
    if p_value < 0.05:
        st.write(f"The time series data is `non-stationary`. adf p-value: {p_value}")
    else:
        st.write(f"The time series data is `stationary`. adf p-value: {p_value}")
    return adf_statistic, p_value

